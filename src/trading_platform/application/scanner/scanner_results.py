from __future__ import annotations

from dataclasses import dataclass
from datetime import UTC, datetime
from decimal import Decimal
from enum import StrEnum
from typing import Protocol


class ScannerResultsState(StrEnum):
    """Application-owned availability state for one scanner result set."""

    READY = "READY"
    NO_DATA = "NO DATA"
    UNAVAILABLE = "UNAVAILABLE"


@dataclass(frozen=True, slots=True)
class ScannerResult:
    """One immutable, provider-independent scanner result row."""

    symbol: str
    signal: str
    score: Decimal
    observed_at: datetime

    def __post_init__(self) -> None:
        _validate_symbol(self.symbol)
        _require_normalized_text(self.signal, "signal", max_length=64)
        _validate_score(self.score)
        if not isinstance(self.observed_at, datetime):
            raise TypeError("observed_at must be datetime")
        if self.observed_at.tzinfo is None or self.observed_at.utcoffset() is None:
            raise ValueError("observed_at must be timezone-aware")
        object.__setattr__(self, "observed_at", self.observed_at.astimezone(UTC))


@dataclass(frozen=True, slots=True)
class ScannerResults:
    """Immutable scanner result set for read-only presentation."""

    state: ScannerResultsState
    source_name: str | None
    results: tuple[ScannerResult, ...]
    detail: str

    def __post_init__(self) -> None:
        if not isinstance(self.state, ScannerResultsState):
            raise TypeError("state must be a ScannerResultsState")
        if not isinstance(self.results, tuple):
            raise TypeError("results must be a tuple")
        if not all(isinstance(result, ScannerResult) for result in self.results):
            raise TypeError("results must contain only ScannerResult values")
        _require_normalized_text(self.detail, "detail", max_length=1_000)

        if self.state is ScannerResultsState.READY:
            _require_normalized_text(self.source_name, "source_name", max_length=200)
            if not self.results:
                raise ValueError("READY scanner results require at least one result")
            symbols = [result.symbol for result in self.results]
            if len(symbols) != len(set(symbols)):
                raise ValueError("READY scanner results require unique symbols")
            return

        if self.results:
            raise ValueError(f"{self.state.value} scanner results must be empty")

        if self.state is ScannerResultsState.NO_DATA:
            _require_normalized_text(self.source_name, "source_name", max_length=200)
            return

        if self.source_name is not None:
            _require_normalized_text(self.source_name, "source_name", max_length=200)

    @classmethod
    def unavailable(
        cls,
        source_name: str | None = None,
        detail: str | None = None,
    ) -> ScannerResults:
        return cls(
            state=ScannerResultsState.UNAVAILABLE,
            source_name=source_name,
            results=(),
            detail=(
                detail
                if detail is not None
                else (
                    "No scanner result source is configured. Results are not "
                    "estimated, reused or generated by the presentation layer."
                )
            ),
        )

    @classmethod
    def no_data(cls, source_name: str) -> ScannerResults:
        return cls(
            state=ScannerResultsState.NO_DATA,
            source_name=source_name,
            results=(),
            detail=(
                "The configured source has not provided scanner results. "
                "No fallback candidates are displayed."
            ),
        )

    @classmethod
    def ready(
        cls,
        source_name: str,
        results: tuple[ScannerResult, ...],
    ) -> ScannerResults:
        return cls(
            state=ScannerResultsState.READY,
            source_name=source_name,
            results=results,
            detail=(
                "Validated scanner results are available from the configured source."
            ),
        )


class ScannerResultsProvider(Protocol):
    """Application port for loading one provider-independent scanner result set."""

    def load_results(self) -> ScannerResults:
        """Load the current read-only scanner result set."""
        ...


class ScannerResultsService:
    """Application service coordinating scanner-result loading through the port."""

    def __init__(self, provider: ScannerResultsProvider) -> None:
        self._provider = provider

    def load_results(self) -> ScannerResults:
        results = self._provider.load_results()
        if not isinstance(results, ScannerResults):
            raise TypeError("Scanner results provider returned an invalid result")
        return results


def _validate_symbol(value: str) -> None:
    _require_normalized_text(value, "symbol", max_length=32)
    if not value.isascii():
        raise ValueError("symbol must use ASCII characters")
    if value != value.upper():
        raise ValueError("symbol must use uppercase characters")
    if not all(character.isalnum() or character in ".-/^" for character in value):
        raise ValueError("symbol contains unsupported characters")


def _require_normalized_text(
    value: str | None,
    field_name: str,
    *,
    max_length: int,
) -> None:
    if not isinstance(value, str):
        raise TypeError(f"{field_name} must be a string")
    if not value or value != value.strip():
        raise ValueError(f"{field_name} must be normalized non-blank text")
    if len(value) > max_length:
        raise ValueError(f"{field_name} must not exceed {max_length} characters")


def _validate_score(value: Decimal) -> None:
    if not isinstance(value, Decimal):
        raise TypeError("score must be Decimal")
    if not value.is_finite():
        raise ValueError("score must be finite")
    if value < Decimal("0") or value > Decimal("100"):
        raise ValueError("score must be between 0 and 100")
